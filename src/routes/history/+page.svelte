		scolastica, in convenzione con l’Amministrazione provinciale di Como e dal 1996 con la sola
		Amministrazione Provinciale di Lecco, per la gestione del servizio di assistenza educativa, sia
		in ambito scolastico che domiciliare a favore dei disabili visivi, avvalendosi della consulenza
		di un’equipe di esperti in ipovisione e in tiflologia organizzando altresì corsi di formazione
		ed aggiornamento per gli assistenti educatori.
	</Paragraph>
